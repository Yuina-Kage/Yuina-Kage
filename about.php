  <h1>パーソナルカラーとは？</h1>
  パーソナルカラーとは、ご自身が持っている肌の色・瞳の色によって生まれ持った「似合う色」のことです。<br>
  パーソナルカラー(似合う色)というのは好きな色でも、キレイな色でもありません。<br>お肌の色にマッチし、
  瞳の輝きがでて元気に見える、お顔がシャープに見える色。お肌の色ムラなどを隠してくれる捕色となる色のことです。
  <br>パーソナルカラーは、「スプリング(春)」「サマー(夏)」「オータム(秋)」「ウィンター(冬)」の
  4つの四季のタイプに分けることができ、<br>自分のタイプがどれなのかを探すのが「パーソナルカラー診断」です。<br>
