  <h2>ブルーベース</h2>
  サマータイプは、爽やかで透明感があり、優しい印象です。<br>スカイブルー、ラベンダー、パステルピンクなど初夏の紫
  陽花の花の色を連想させるような、パステル調で淡くソフトな色が似合います。<br>肌の色は、日焼けすると赤くなります
  がすぐ戻ります。瞳の色は、優しい感じのブラックです。<br>髪の色（地毛）は、ソフトブラウン、赤みがかったブラ
  ックの人が多いです。<br>おすすめのスーツ(ジャケット)カラーは、紫みがかったネイビー、明るいグレー、赤みのブラウ
  ン、ブルー系になります。<br>シャツカラーは、白無地の場合、少しグレイッシュなオフホワイトやミルキーホワイトが似
  合います。黄みがなく、真っ白過ぎない優しい色味の白を選ぶようにしてください。<br>アクセサリー(時計や眼鏡)は、
  明るいシルバー、グレー、ブルーグレーが似合います。<br>似合うヘアカラー&ヘアスタイルは、ピンク系のココアブラウ
  ンやローズブラウンなど落ち着いた赤みの色で、ふんわり柔らかいヘアスタイルだと、よりソフトであか抜けた印象になり
  ます。

