  <h1>スプリングタイプ</h1>
  <h2>イエローベース</h2>
  スプリングタイプは、健康的で親しみやすく、若々しい印象です。<br>明るい黄緑、ライトブラウン、コーラルピンク
  やイエローなど春に色づいた花壇や、野原を連想させるようなカラフルで明るい暖色系の色が似合います。<br>肌の色は
  、少し黒くなるが、割とすぐに白く戻ります。瞳の色は、イエロー系のブラウンです。<br>髪の色（地毛）は、明る
  ブラウンや、黄みがかったブラウンの人が多いです。<br>おすすめのスーツ(ジャケット)カラーは、明るいネイビー、
  ライトブラウン、明るいカーキ系になります。<br>シャツカラーは、白無地の場合、真っ白なピュアホワイトのような色
  は血色が悪く浮いて見えてしまうので、黄みよりでアイボリー系の白を選ぶようにしてください。<br>アクセサリー(時計
  や眼鏡)は、明るいゴールド、ブラウン、ベージュ系が似合います。<br>似合うヘアカラー&ヘアスタイルは、ライトベージ
  ュやライトブラウンなど、明るいイエロー系の色で、動きのある短めのスタイルでいきいきと見せると、より印象が良くな
  ります。

