  <h1>オータムタイプ</h1>
  <h2>イエローベース</h2>
  オータムタイプは、落ち着いていて大人っぽく、ワイルドな印象です。<br>アースカラー、レンガ色、マスタード、モスグ
  リーンなど実りの秋を連想させる木の実の色や紅葉など、暖色系の濃く渋い色が似合います。<br>肌の色は、すぐ黒くな
  りやすい上に戻りにくいです。瞳の色は、ダークブラウンです。<br>髪の色(地毛)は、深みのあるダークブラウンの
  人が多いです。<br>おすすめのスーツ(ジャケット)カラーは、深みのあるネイビー、ダークブラウン、濃いカーキになりま
  す。<br>シャツカラーは、白無地の場合、黄みの強い生成り色やオイスターホワイトが似合います。真っ白なピュアホワイ
  トや青みがかった色は、顔色が浮いてしまうので注意してください。<br>アクセサリー(時計や眼鏡)は、濃いゴールド、ス
  モークブラウン、濃いベージュ系が似合います。<br>似合うヘアカラー&ヘアスタイルは、スモーキーなダークブラウンや
  ダークゴールドなど黄みの強い落ち着いた色で、ナチュラルな雰囲気が似合います。<br>長髪で束ねるようなスタイルもオ
  ータムタイプの方は自然とマッチするでしょう。


