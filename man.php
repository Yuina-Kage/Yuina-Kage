  <h1>ウィンタータイプ</h1>
  <h2>ブルーベース</h2>
  ウィンタータイプは、キリっとしていて、クールでシャープな印象です。<br>白と黒のモノトーンや、トゥルーレッドやイ
  エローなどの原色といった、真っ白な雪やゲレンデに映えるビビットなスキーウエアの色、イルミネーションの色のような
  コントラストの強い澄んだ寒色系の色が似合います。<br>肌の色は、日焼けすると赤くなりやすいです。瞳の色は、
  はっきりとしたブラックやダークブラウンです。<br>髪の色(地毛)は、はっきりとしたダークブラウン、赤みのブラック
  の人が多いです。<br>おすすめのスーツ(ジャケット)カラーは、ダークネイビー、ブラック、ダークグレーになります。
  <br>シャツカラーは、白無地の場合、真っ白な純白のピュアホワイトが似合います。少しくすんでいたり、クリーム系のホ
  ワイトは、寂しく見えてしまうので注意です。<br>アクセサリー(時計や眼鏡)は、シルバー、プラチナ、グレー、ブルーグ
  レー系が似合います。<br>似合うヘアカラー&ヘアスタイルは、ブラックブラウンや少し赤みのある濃いローズプラウンが
  似合います。真っ黒の黒髪が似合うのもウィンタータイプの特徴です。全体が重くならないように、短めで動きのあるヘア
  スタイルにすると良いでしょう。

